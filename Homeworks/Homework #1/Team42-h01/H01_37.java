import java.util.ArrayList;
public class H01_37
{
 public static void arrayListRemove( ArrayList<Integer> pList, int pValue)
{
    for (int i=0; i<pList.size(); i++)
    {
        if (pList.get(i) == pValue);
        {
            pList.remove(i);
        }
    }
}

}
